class Solution:
    def postorderTraversal(self, root: TreeNode) -> List[int]:
        res = []
        if not root:
            return res
        stack = [root]
        op_satck = [0]
        while stack:
            if op_satck[-1] == 0:
                op_satck[-1] = 1
                node = stack[-1]
                if node.right:
                    stack.append(node.right)
                    op_satck.append(0)
                if node.left:
                    stack.append(node.left)
                    op_satck.append(0)
            else:
                res.append(stack.pop().val)
                op_satck.pop()
        return res

